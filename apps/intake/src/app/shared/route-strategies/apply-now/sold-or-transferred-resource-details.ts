import { RoutePath } from '..';
import { IRoutable } from '../strategy';

export class ApplyNowResourcesSoldOrTransferredResourceDetailsStrategy implements IRoutable {
    hideBackButton = true;
    hideNextButton = true;
    get currentRoute(): string {
        return RoutePath.APPLYNOW + '/' + RoutePath.APPLYNOW_RESOURCES + '/' + RoutePath.APPLYNOW_RESOURCES_SOLDORTRANSFERREDRESOURCEDETAILS;
    }

    nextRoute(): string {
        return RoutePath.APPLYNOW + '/' + RoutePath.APPLYNOW_RESOURCES + '/' + RoutePath.APPLYNOW_RESOURCES_SOLDORTRANSFERREDRESOURCESUMMARY;
    }

    previousRoute(): string {
        return RoutePath.APPLYNOW + '/' + RoutePath.APPLYNOW_RESOURCES + '/' + RoutePath.APPLYNOW_RESOURCES_LIFEINSURANCEPOLICIESSUMMARY;
    }
}
