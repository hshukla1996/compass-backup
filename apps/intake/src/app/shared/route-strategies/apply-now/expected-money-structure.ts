import { RoutePath } from '..';
import { IRoutable } from '../strategy';

export class ApplyNowExpectedMoneyStructureStrategy implements IRoutable {
    hideBackButton = true;
    hideNextButton = true;
    get currentRoute(): string {
        return RoutePath.APPLYNOW + '/' + RoutePath.APPLYNOW_RESOURCES + '/' + RoutePath.APPLYNOW_RESOURCES_EXPECTEDMONEYSTRUCTURE;
    }

    nextRoute(): string {
        return RoutePath.APPLYNOW + '/' + RoutePath.APPLYNOW_RESOURCES + '/' + RoutePath.APPLYNOW_RESOURCES_EXPECTEDMONEYSTRUCTURESUMMARY;
    }

    previousRoute(): string {
        return RoutePath.APPLYNOW + '/' + RoutePath.APPLYNOW_RESOURCES + '/' + RoutePath.APPLYNOW_RESOURCES_NONRESIDENTIALPROPERTYSUMMARY;
    }
}
